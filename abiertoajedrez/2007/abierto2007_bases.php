          <h4><br />
            1. CALENDARIO</h4> 
            <p>5 al 9 de Diciembre de 2007. </p>
            <ul>
              <li> D&iacute;a 5, una ronda, desde las 17.30 horas.<br /></li>
                <li>D&iacute;as 6 a 8, dos rondas diarias, a las 10.30 y a las 17.30 horas. <br /></li>
               <li> D&iacute;a 9, a las 9.30 y a las 15.30 horas.<br /></li>
                <li>Entrega de premios: D&iacute;a 9 (domingo), aproximadamente a las 20.30 horas.</li>
            </ul>
            <br />
            <h4>2. SISTEMA</h4> 
            <p>Suizo, a 9 rondas. <br />
            Para el orden de fuerza se tomar&aacute; la m&aacute;s alta evaluaci&oacute;n elo en vigor del jugador, entre las listas FIDE, FEDA o FADA. En su defecto, se atender&aacute; a los mismos criterios sobre evaluaci&oacute;n que se describen en el punto 11.</p>
            <p>&nbsp;            </p>
            <h4>3. PAREO</h4>
            <p>Pareo informatizado.<br />
            El programa y los par&aacute;metros se comunicar&aacute;n antes del inicio del torneo. No se admitir&aacute;n reclamaciones sobre el pareo, excepto por entrada err&oacute;nea de datos o par&aacute;metros. </p>
            <p>&nbsp;            </p>
            <h4>4. EXENCIONES (BYES)</h4>
            <p>Concepto: Los jugadores podr&aacute;n recibir descansos autorizados o exenciones (byes) para un m&aacute;ximo de tres rondas, a petici&oacute;n propia y sin necesidad de ninguna justificaci&oacute;n. No ser&aacute;n emparejados en las rondas solicitadas, recibiendo medio punto por cada exenci&oacute;n, que se entiende sin asignaci&oacute;n de color.</p>
              <p> Requisitos: a) pedirlo por escrito, o en email o fax de inscripci&oacute;n, antes de la primera ronda; b) m&aacute;ximo de tres exenciones, s&oacute;lo entre las cuatro primeras rondas; d) cuota abonada.</p>
              <p> Exclusi&oacute;n: Se podr&aacute; excluir de este derecho, total o parcialmente, a los jugadores invitados que reciban ayudas o primas econ&oacute;micas de la Organizaci&oacute;n.</p>
              <p> Cancelaci&oacute;n: El jugador podr&aacute; cancelar alguna o todas las exenciones que hubiera solicitado. Debe hacerlo por escrito, bien antes de comenzar el torneo o, una vez comenzado, dentro de la jornada anterior a la de la exenci&oacute;n.</p>
              <p>&nbsp;            </p>
            <h4>5. RITMO</h4>
            <p> Cada jugador dispondr&aacute; de 90 minutos de 
              tiempo principal de reflexi&oacute;n y de 30 
              segundos adicionales por cada jugada.</p>
            <p>&nbsp;            </p>
            <h4>6. DESEMPATES 
              Orden de prelaci&oacute;n: </h4>
            <p>1. Penalizaci&oacute;n de acuerdos prematuros: En caso de empate decide la puntuaci&oacute;n final tras valorar como cero, al &uacute;nico efecto de este desempate, las tablas por acuerdo prematuro en cualquiera de las &uacute;ltimas cinco rondas.<br />
            Se considerar&aacute; prematuro, a este &uacute;nico efecto, cuando se produzca antes de la realizaci&oacute;n de 25 jugadas - que no constituyan una repetici&oacute;n de jugadas, reclamada o no, con el manifiesto prop&oacute;sito de sobrepasar dicho l&iacute;mite - o antes de cumplirse una hora de sesi&oacute;n de juego. Se publicar&aacute; la relaci&oacute;n de las partidas que supongan acuerdo prematuro.</p>
            <p> La propuesta de tablas siempre debe ser anotada en la planilla. Los jugadores comunicar&aacute;n su acuerdo al &aacute;rbitro y mantendr&aacute;n los tiempos en los relojes de la partida hasta su constancia por el &aacute;rbitro, que indicar&aacute; a los jugadores si el acuerdo es o no prematuro a efectos de desempate.</p>
            <p> Este sistema respeta el derecho al acuerdo reglamentario de tablas (arts. 5.2.c), y 9.1, Leyes del Ajedrez) y a la puntuaci&oacute;n (art. 11 L.A.). </p>
            <p>2. Los restantes sistemas de desempate se publicar&aacute;n antes del inicio del torneo, conjuntamente con el programa inform&aacute;tico y los par&aacute;metros de pareo. </p>
            <p>&nbsp;            </p>
            <h4>7. DESCALIFICACI&Oacute;N</h4>
            <p>La incomparecencia de un jugador a una 
              ronda causar&aacute; su eliminaci&oacute;n del torneo, salvo que excuse su ausencia y solicite por 
              escrito proseguir en el torneo. 
              Dos incomparecencias suponen la 
              descalificaci&oacute;n definitiva; as&iacute; como la falta 
              grave deportiva o el impago de la cuota 
              correspondiente.</p>
            <p>&nbsp;            </p>
            <h4>8. COMIT&Eacute; DE APELACI&Oacute;N</h4>
            <p>Las decisiones del Arbitro Principal son 
              recurribles oportunamente, por escrito, en 
              ning&uacute;n caso m&aacute;s tarde de 30 minutos tras 
              finalizar la sesi&oacute;n en la que se produjo la 
              decisi&oacute;n. </p>
            <p>El Comit&eacute; estar&aacute; constituido por el Director 
              del Torneo, cuyo voto es dirimente; por el 
              &Aacute;rbitro Supervisor, si est&aacute; presente; y por 
              cuatro jugadores elegidos por sorteo. Se 
              elegir&aacute;n suplentes para el caso de estar 
              ausente o directamente implicado un 
              miembro titular.</p>
            <p>&nbsp;            </p>
            <h4>9. REGLAS DE JUEGO</h4>
            <p>Todos est&aacute;n obligados a seguir una conducta deportiva acorde con las Leyes del Ajedrez, el C&oacute;digo de &Eacute;tica de FIDE, las presentes Bases, y las instrucciones del &Aacute;rbitro Principal. </p>
            <p> El jugador debe comprobar la puntuaci&oacute;n, ronda a ronda y, en caso de error, reclamar&aacute; inmediatamente, en todo caso antes del pareo siguiente. Podr&aacute;n no ser admitidas reclamaciones posteriores.</p>
            <p> Cada jugador debe revisar sus datos en los listados oficiales. Si no lo hace as&iacute; podr&aacute; rechazarse una reclamaci&oacute;n de dicho jugador, a efectos de premios y en perjuicio de terceros, planteada despu&eacute;s de publicado el pareo de la &uacute;ltima ronda.</p>
            <p>&nbsp;            </p>
            <h4>10. INSCRIPCI&Oacute;N Y CUOTAS</h4>
            <p>Plazo: Hasta el 4 de Diciembre. Aforo limitado seg&uacute;n orden de inscripci&oacute;n, excepto los maestros invitados. Ser&aacute; discrecional admitir inscripciones posteriores.</p>
            <p> Cuotas: 40 &euro;, general; y 20 &euro;, granadinos (residentes habituales o federados en Granada). </p>
            <p> Ingreso en cuenta 2031 0188 97 0200477338, indicando nombre y apellidos, fecha de nacimiento y procedencia. La inscripci&oacute;n debe confirmarse por tel&eacute;fono en el 958 08 17 14 (ma&ntilde;anas) o al 639 93 50 07 (Sergio Cuesta) o a info@egajedrez.com</p>
            <p> Condiciones a maestros: 639 93 50 07</p>
            <p>&nbsp;            </p>
            <h4>11. PREMIOS</h4>
            <p>Los premios en met&aacute;lico no son acumulables entre s&iacute; y se optar&aacute; por el de mayor cuant&iacute;a; a igual cuant&iacute;a, el primero en el orden de presentaci&oacute;n en la tabla de premios (de arriba abajo y columnas de izquierda a derecha).<br />
Los trofeos van ligados al puesto real obtenido y, en consecuencia, son acumulables, a excepci&oacute;n de los trofeos relativos a edad o a procedencia, entre s&iacute; respectivamente, en que se optar&aacute; al primero que figure en la tabla de trofeos.</p>
            <p> A efectos de premios por tramos se tomar&aacute; la m&aacute;s alta evaluaci&oacute;n elo (rating) del jugador entre las listas oficiales en vigor FIDE o FEDA o FADA; y s&oacute;lo si no tiene evaluaci&oacute;n en esas listas, la que tenga en otra federaci&oacute;n auton&oacute;mica espa&ntilde;ola; y si tambi&eacute;n carece de alguna de las anteriores, siendo extranjero, la que tenga en su federaci&oacute;n nacional. En el caso de jugadores deslistados se tomar&aacute; su &uacute;ltimo rating conocido, salvo que en lista FIDE, FEDA o FADA disponga de evaluaci&oacute;n actualizada.</p>
            <p> Los premios por edades (Veteranos, Sub23, Sub 16, etc.) seguir&aacute;n el criterio federativo, referido al a&ntilde;o 2007. Se entiende por sub 2300, el que tenga 2300 o menos, y as&iacute; sucesivamente en cada tramo.</p>
            <p>&nbsp;            </p>
            <h4>12. RECEPCI&Oacute;N DE PREMIOS</h4>
            <p>Para tener derecho al premio el jugador lo aceptar&aacute; y recoger&aacute; personalmente o mediante un representante previamente acordado con la Organizaci&oacute;n, en el acto p&uacute;blico de entrega de premios.</p>
            <p>Elabora: Guillermo M. Barranco<br />
            Supervisa: Ismael Nieto. </p>
