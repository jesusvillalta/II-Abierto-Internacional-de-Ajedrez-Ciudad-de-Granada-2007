            <h4>INFORMACI&Oacute;N DE INTER&Eacute;S</h4>
            <p>BREVE INTRODUCCI&Oacute;N <br />
              Granada, Ciudad c&eacute;lebre hasta la mitificaci&oacute;n, es resultado de las civilizaciones occidental y oriental, que tuvieron en ella un punto de encuentro en un lugar privilegiado. 
              Capital del antiguo reino Nazar&iacute;, al pie de Sierra Nevada, limitada por &quot;la Vega&quot; y atravesada 
              por los r&iacute;os Genil y Darro que divide las colinas de la Alhambra y el Albayzin. </p>
            <p>Entre desniveles de 650 y 800 m, Granada y su Vega se sit&uacute;an bajo Sierra Nevada. Este 
              paisaje determina sus vistas panor&aacute;micas. La ciudad antigua conserva el ambiente urbano con 
              pintorescos rincones, dominados por la Alhambra, con la que establece relaciones que trascienden de lo visual.            </p>
            <p>Su Audiencia Territorial, sede del Tribunal Superior de Justicia de Andaluc&iacute;a, Universidad, 
              Arzobispado y su situaci&oacute;n geogr&aacute;fica centro de su comarca y Andaluc&iacute;a Oriental, la 
              determinan como ciudad de servicios, tur&iacute;stica y agr&iacute;cola. El centro, comercial y administrativo. 
              El ensanche universitario, animado de j&oacute;venes. El Albayz&iacute;n isl&aacute;mico y el Sacromonte, con 
              cuevas que habitaban los gitanos y sus t&iacute;picas zambras, genuinas fiestas folkl&oacute;ricas.</p>
            <p>&nbsp;</p>
