            <p> ALHAMBRA Y EL GENERALIFE <br />
              La Alhambra, cuyo significado &aacute;rabe viene a significar algo as&iacute; como&ldquo;La Roja&rdquo; es una ciudad amurallada real (medina) que ocupa la 
mayor parte del cerro de tierra roja de La Sabika. Aparte, la ciudad 
ten&iacute;a una gran muralla exterior de la que a&uacute;n se conservan restos 
por lo que se cree que la Alhambra pod&iacute;a funcionar con autonom&iacute;a 
respecto de Granada. En la Alhambra se encontraban todos los 
servicios propios y necesarios para la poblaci&oacute;n que viv&iacute;a all&iacute;: 
mezquitas, escuelas, talleres, etc. El Generalife es una villa de 
verano utilizada por los reyes musulmanes de Granada como lugar 
de descanso. Un simple paseo desde la sala de juego nos sumerge 
en la belleza de sus jardines. </p>
            <p> ALBAYZIN <br />
              Se asienta sobre la estribaci&oacute;n oeste del Cerro de San Miguel, en la 
              margen derecha del r&iacute;o Darro y frente a la colina de la Alhambra. 
              Alberga el m&aacute;s antiguo asentamiento humano de la ciudad de 
              Granada y representa uno de los barrios t&iacute;picos de la arquitectura 
              Nazar&iacute;. Fue caracter&iacute;stico de dicho barrio en la &eacute;poca &aacute;rabe la 
              canalizaci&oacute;n y distribuci&oacute;n del agua potable a trav&eacute;s de aljibes, 
              dep&oacute;sitos destinados a conservar el agua de lluvia; una gran 
              mayor&iacute;a sigue a&uacute;n en uso. El tipo tradicional de vivienda es el 
              carmen, compuesto por una vivienda rodeada por un alto muro que 
              la separa de la calle y que incluye un peque&ntilde;o huerto o jard&iacute;n. </p>
            <p>CATEDRAL <br />
              La Catedral de Granada una de las obras cumbres del Renacimiento 
              espa&ntilde;ol. Diego de Silo&eacute; traz&oacute; en 1529 las l&iacute;neas renacentistas de 
              todo el edificio sobre los cimientos g&oacute;ticos, con girola y cinco naves 
              en lugar de las tres habituales. Su construcci&oacute;n se prolong&oacute; durante 
              181 a&ntilde;os. Alonso Cano reform&oacute; en 1667 el proyecto inicial de la 
              fachada principal, introduciendo elementos barrocos: el proyecto no 
              fue terminado por diversos problemas, entre ellos econ&oacute;micos, por lo 
              que finalmente la Catedral qued&oacute; con una torre, formada solo por 
              tres cuerpos en lugar de los seis previstos.            </p>
